function showWMS() {

    var click = document.getElementById('drop__content--WMS');
    var arrowWMS = document.getElementById('arrow--WMS');

    if (click.style.display === "none") {
        click.style.display = "block";
    } else {
        click.style.display = "none";
    }

    if (arrowWMS.style.transform == "rotate(90deg)") {
        arrowWMS.style.transform = "rotate(0deg)";
    } else {
        arrowWMS.style.transform = "rotate(90deg)";
    }
}

